/*
*******************************************************************************
Преобразование Уолша-Адамара

Пусть X -- последовательность из 2^n битов. Преобразование Уолша-Адамара
(Walsh-Hadamard Transform, WHT) этой последовательности выполняется следующим
образом:
	Y <- (-1)^X H_n.
Здесь
* (-1)^X -- вектор, полученный заменой в X каждого бита b на число (-1)^b;
* H_n -- матрица Адамара, которая определяется рекурсивно по формуле
	H_i <- (H_{i-1} H_{i-1}\\ H_{i-1} -H_{i-1})
  с начальным условием H_0 <- (1).

Вектор Y называется спектром Уолша-Адамара, координаты Y -- спектральными
коэффициентами. Пусть S(X) --- максимальный по модулю коэффициент.

Известно [Rod06,LitShp08], что если X --- случайная последовательность, то
	S(X) \sim sqrt{2 * 2^n ln(2^n)}.

\remark Последовательность X задается строкой октетов: первый бит X -- это
младший бит первого октета, последний бит -- старший бит последнего октета.

[Rod06]    Rodier F. Asymptotic nonlinearity of Boolean functions,
		   Des. Codes Cryptogr., 40 (2006), pp. 59–70.
[LitShp08] Litsyn S., Shpunt A. On the distribution of Boolean function
		   nonlinearity. SIAM J. Discrete Math., 23 (2008), no. 1, pp. 79-95.
*******************************************************************************
*/

