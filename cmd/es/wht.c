/*
*******************************************************************************
Преобразование Уолша-Адамара

Преобразование Уолша-Адамара применяется к последовательности, построенной 
по битам буфера [count]buf заменой бита b на число (-1)^b. Определяется 
максимальный по модулю коэффициент преобразования.

\pre count -- степень двойки.
\pre count <= 2^{27} (коэффициенты Уолша-Адамара укладываются в 32 бита).
\pre memIsValid(stack, count * 32).
\pre Буферы buf и stack не пересекаются.
\return Максимальный по модулю коэффициент Уолша-Адамара.
\remark Первый элемент обрабатываемой последовательности строится по младшему 
биту первого октета buf, последний элемент последовательности -- по старшему
биту последнего октета buf.
*******************************************************************************
*/

u32 rngWht(const octet* buf, size_t count, void* stack)
{
	size_t i, j, h;
	i32* t;
	i32 max;
	// pre
	ASSERT(0 < count && (count & (count - 1)) == 0);
	ASSERT(B_PER_S < 32 || count <= SIZE_BIT_POS(27));
	ASSERT(count < SIZE_MAX / 8 / 4);
	ASSERT(memIsDisjoint2(buf, count, stack, count * 8 * 4));
	// обработать октеты
	t = (i32*)stack;
	for (i = 0; i < count; ++i)
		for (j = 0; j < 8; ++j)
			t[8 * i + j] = _wht8[buf[i]][j];
	// быстрое преобразование Уолща-Адамара
	for (h = 8; h < 8 * count; h *= 2)
		for (i = 0; i < 8 * count; i += h * 2)
			for (j = 0; j < h; ++j)
			{
				register i32 x = t[i + j];
				register i32 y = t[i + j + h];
				t[i + j] = x + y;
				t[i + j + h] = x - y;
			}
	// максимальный коэффициент
	max = 0;
	for (i = 0; i < count * 8; ++i)
	{
		if (t[i] > max)
			max = t[i];
		else if (t[i] < -max)
			max = -t[i];
	}
	return (u32)max;
}
